console.log(Math); // Math object in JavaScript
console.log(Math.PI); // Value of PI
console.log(Math.E); // Value of Euler's number
console.log(Math.SQRT2); // Square root of 2
console.log(Math.SQRT1_2); // Square root of 1/2
console.log(Math.LN2); // Natural logarithm of 2
console.log(Math.LN10); // Natural logarithm of 10
console.log(Math.LOG2E); // Base-2 logarithm of E

console.log(Math.abs(-5)); // Absolute value of -5
console.log(Math.ceil(4.2)); // Round up to the nearest integer
console.log(Math.floor(4.8)); // Round down to the nearest integer
console.log(Math.round(4.5)); // Round to the nearest integer
console.log(Math.max(1, 2, 3, 4, 5)); // Maximum value
console.log(Math.min(1, 2, 3, 4, 5)); // Minimum value
console.log(Math.random()); // Random number between 0 and 1
console.log(Math.pow(2, 3)); // 2 raised to the power of 3

const min  = 10
const max = 20
console.log(Math.floor(Math.random() * (max - min + 1)) + min); // Random number between 10 and 20